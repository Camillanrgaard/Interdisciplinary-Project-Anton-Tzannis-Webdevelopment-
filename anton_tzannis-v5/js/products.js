//AMERICAN CREW
document.getElementById("americancrew-section").addEventListener("mouseover", function () {
  (document.getElementById("americancrew-btn").style.height = "250"),
    (document.getElementById("americancrew-info").style.display = "block"),
    (document.getElementById("americancrew-info-btn").style.display = "block");
});
document.getElementById("americancrew-section").addEventListener("mouseout", function () {
  (document.getElementById("americancrew-btn").style.height = "50"),
    (document.getElementById("americancrew-info").style.display = "none"),
    (document.getElementById("americancrew-info-btn").style.display = "none");
});

